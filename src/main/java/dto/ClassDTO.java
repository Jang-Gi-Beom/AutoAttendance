package dto;

public class ClassDTO {
	
	private int postNum;
	private String lecture_name;
	private String lecture_check;
	private String subject;
	private String subject_img;
	private String subject_saveImg;
	private String class_date;
	private String professor;
	
	public int getPostNum() {
		return postNum;
	}
	public void setPostNum(int postNum) {
		this.postNum = postNum;
	}
	public String getLecture_name() {
		return lecture_name;
	}
	public void setLecture_name(String lecture_name) {
		this.lecture_name = lecture_name;
	}
	public String getLecture_check() {
		return lecture_check;
	}
	public void setLecture_check(String lecture_check) {
		this.lecture_check = lecture_check;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getSubject_img() {
		return subject_img;
	}
	public void setSubject_img(String subject_img) {
		this.subject_img = subject_img;
	}
	public String getSubject_saveImg() {
		return subject_saveImg;
	}
	public void setSubject_saveImg(String subject_saveImg) {
		this.subject_saveImg = subject_saveImg;
	}
	public String getClass_date() {
		return class_date;
	}
	public void setClass_date(String class_date) {
		this.class_date = class_date;
	}
	public String getProfessor() {
		return professor;
	}
	public void setProfessor(String professor) {
		this.professor = professor;
	}
	

}
